package com.sensing.core.bean;

import java.io.Serializable;
import java.util.List;

public class TaskResp implements Serializable {
   private Task task;
   private List<TaskChannelResp> taskChannel;

    public Task getTask() {
        return task;
    }

    public void setTask(Task task) {
        this.task = task;
    }

    public List<TaskChannelResp> getTaskChannel() {
        return taskChannel;
    }

    public void setTaskChannel(List<TaskChannelResp> taskChannel) {
        this.taskChannel = taskChannel;
    }
}
