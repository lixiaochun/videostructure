package com.sensing.core.bean;

import java.io.Serializable;
import java.util.List;

public class SysRoleSaveReq implements Serializable {
    private List<Integer> sysResourceIds;
    private String roleName;
    private String userUUid;//user的uuid
    private Integer roleId;//角色id
    private Integer isDeleted;


    public Integer getIsDeleted() {
        return isDeleted;
    }

    public void setIsDeleted(Integer isDeleted) {
        this.isDeleted = isDeleted;
    }

    public String getUserUUid() {
        return userUUid;
    }

    public void setUserUUid(String userUUid) {
        this.userUUid = userUUid;
    }

    public Integer getRoleId() {
        return roleId;
    }

    public void setRoleId(Integer roleId) {
        this.roleId = roleId;
    }

    public List<Integer> getSysResourceIds() {
        return sysResourceIds;
    }

    public void setSysResourceIds(List<Integer> sysResourceIds) {
        this.sysResourceIds = sysResourceIds;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }
}
